class Solution {
public:
    vector<int> partitionLabels(string s) {
        int n = s.size();
        unordered_map<char, int> lastIndex;
        for(int i = 0; i < n; i++){
            lastIndex[s[i]] = i;
        }

        vector<int> result;
        int size = 0;
        int end = 0;
        for(int i = 0; i < n; i++){
            end = max(end, lastIndex[s[i]]);
            size++;
            if(i == end){
                result.push_back(size);
                size = 0;
            }
        }
        return result;
    }
};